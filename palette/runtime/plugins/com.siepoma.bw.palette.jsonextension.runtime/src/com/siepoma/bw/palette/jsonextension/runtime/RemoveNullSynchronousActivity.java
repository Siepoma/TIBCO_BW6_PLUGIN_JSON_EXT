public static String clearJSON(String json) throws Exception{
		
		String outJson = null;
		try {
				JSONArray jarray = new JSONArray(json);
				outJson = clearJSONArray(jarray);
		}catch (Exception e) {
			try {
				JSONObject jobj = new JSONObject(json);
				outJson = clearJSONObj(jobj);
			} catch (Exception e2) {
				throw e2; 
			}
			
		}
		//System.out.println(outJson);
		return outJson;
	}
	
	
	private static String clearJSONObj(JSONObject jobj) {
		
		Iterator<String> keys = jobj.keys();
		while( keys.hasNext() ) {
			String keyname = (String)keys.next();
			Object keyvalue = jobj.get(keyname);
			
			if(keyvalue instanceof JSONObject) {
				
				//System.out.println("ClearObj " + keyvalue);
				clearJSONObj((JSONObject)keyvalue);

				
			}
			else if (keyvalue instanceof JSONArray) {
				
				//System.out.println("ClearArray " + keyvalue);
				clearJSONArray((JSONArray)keyvalue);
			}
			else
			{
				if(keyvalue.toString().toLowerCase().equals(NULL_VALUE))
				{
					//System.out.println(keyname+"\t"+keyvalue);
					keys.remove();
				}
			}
		}
		return jobj.toString();
		
		
		
	}
	
	private static String clearJSONArray(JSONArray jarray) {

		Iterator<Object> keys = jarray.iterator();
		Object elem;
		while (keys.hasNext()) {
			elem = keys.next();

			if(elem instanceof JSONObject) {
				JSONObject jObj = (JSONObject) elem;
				clearJSONObj(jObj);
			}
			else if(elem instanceof JSONArray) {
				JSONArray jArr = (JSONArray) elem;
				clearJSONArray(jArr);
			}else {
				if (elem.toString().toLowerCase().equals(NULL_VALUE)) {
					keys.remove();
				}
			}
			
		}
		return jarray.toString();

	}
	

